/*As classes na programação são como receitas de um objeto que contém caracteristicas
e comportamentos, nelas você consegue armazenar propriedades e métodos.
*/

public class Hello {
    public static void main(String args[]) {
        System.out.println("Hello Word!");
    }
}

/*
- O public define que a classe é pública, portanto, qualquer um no código pode acessar ela
- o static é para dizer que o membro pode ser acessado sem a necessidade de instanciar a classe
- void quer dizer que não retorna nada
- main quer dizer que é a classe principal, ela é a única função necessária para que o compilador identifique aonde irá iniciar o arquivo
- System.out é um objeto para saída padrão,  println é para mostrar algo na tela que será passado como parâmetro dentro dos parênteses no nosso caso é uma String (texto)
*/